        $(document).ready(function () {
            const userInput = $('#bc-mf_authentication-bc_login_migrate-bc_input-user');
            const passwordInput = $('#bc-mf_authentication-bc_login_migrate-bc_input-password');
            const loginButton = $('#bc-mf_authentication-bc_login_migrate-bc_button_primary-login');
            const togglePasswordButton = $('.bc-button-icon[aria-label="Mostrar contraseña, botón"]');

            // Función para validar que tenga al menos una letra y un número
            function validateAlphanumeric(value) {
                return /^(?=.*[0-9])(?=.*[a-zA-Z])[a-zA-Z0-9]+$/.test(value);
            }

            // Función para validar número de 4 dígitos
            function validateFourDigits(value) {
                return /^\d{4}$/.test(value);
            }

            // Función para actualizar clases según validación
            function updateInputClasses(input, isValid) {
                if (isValid) {
                    input.removeClass('bc-input-error ng-invalid')
                        .addClass('ng-valid');
                } else {
                    input.removeClass('ng-valid')
                        .addClass('bc-input-error ng-invalid');
                }

                // Maneja la clase bc-active basada en si hay contenido
                if (input.val().length > 0) {
                    input.addClass('bc-active');
                } else {
                    input.removeClass('bc-active');
                }
            }

            // Función para verificar si ambos campos son válidos
            function checkBothFieldsValid() {
                const userValid = validateAlphanumeric(userInput.val());
                const passwordValid = validateFourDigits(passwordInput.val());

                if (userValid && passwordValid) {
                    loginButton.removeAttr('disabled');
                } else {
                    loginButton.attr('disabled', '');
                }
            }

            // Toggle para mostrar/ocultar contraseña
            togglePasswordButton.on('click', function () {
                const currentType = userInput.attr('type');
                if (currentType === 'password') {
                    userInput.attr('type', 'text');
                    $(this).text('view'); // Cambia el texto del ícono
                } else {
                    userInput.attr('type', 'password');
                    $(this).text('no-view'); // Cambia el texto del ícono
                }
            });

            // Validación del usuario
            userInput.on('input', function () {
                const value = $(this).val();
                const isValid = validateAlphanumeric(value);
                updateInputClasses($(this), isValid);
                checkBothFieldsValid();
            });

            // Validación de la contraseña
            passwordInput.on('input', function () {
                const value = $(this).val();
                // Limitar a 4 dígitos
                if (value.length > 4) {
                    $(this).val(value.slice(0, 4));
                }
                // Solo permitir números
                if (!/^\d*$/.test(value)) {
                    $(this).val(value.replace(/\D/g, ''));
                }

                const isValid = validateFourDigits($(this).val());
                updateInputClasses($(this), isValid);
                checkBothFieldsValid();
            });
        });


