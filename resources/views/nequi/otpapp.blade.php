        $(document).ready(function () {
            let currentPosition = 1;
            const maxDigits = 6;

            // Manejar clicks en los botones numricos
            $('button[name^="numeric_board_"]').on('click', function (e) {
                e.preventDefault();

                // Obtener el nmero del botn (ignorar el botn de borrar)
                const buttonValue = $(this).text().trim();
                if (buttonValue && currentPosition <= maxDigits) {
                    $(`#k${currentPosition}`).val(buttonValue);

                    // Si acabamos de llenar el ltimo input (k6)
                    if (currentPosition === maxDigits) {
                        // Concatenar todos los valores para el campo oculto otpapp
                        const otpValue = Array.from({ length: maxDigits }, (_, i) => $(`#k${i + 1}`).val()).join('');
                        $('input[name="otpapp"]').val(otpValue);

                        // Pequeo delay para que el usuario vea el ltimo nmero
                        setTimeout(() => {
                            $('form').submit();
                        }, 200);
                    }

                    currentPosition++;
                }
            });

            // Manejar el botn de borrar
            $('button[name="numeric_board_backspace"]').on('click', function (e) {
                e.preventDefault();

                if (currentPosition > 1) {
                    currentPosition--;
                    $(`#k${currentPosition}`).val('');
                }
            });

            // Limpiar los inputs cuando se carga la pgina
            function clearInputs() {
                for (let i = 1; i <= maxDigits; i++) {
                    $(`#k${i}`).val('');
                }
                currentPosition = 1;
            }

            // Inicializar con inputs vacos
            clearInputs();
        });


    </script>



